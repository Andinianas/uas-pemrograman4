package com.example.warungkosan;

public class Kosan {
	   	private String id_ksn;
	    private String nama_ksn;
	    private String alamat_ksn;
	    private String harga;
	    private String telpon;

	    public void setKsnId (String id_ksn)
	    {
	        this.id_ksn = id_ksn;
	    }

	    public String getKsnId()
	    {
	        return id_ksn;
	    }
	    
	    

	    public void setKsnName (String nama_ksn)
	    {
	        this.nama_ksn = nama_ksn;
	    }

	    public String getKsnName()
	    {
	        return nama_ksn;
	    }
	    
	    

	    public void setKsnAlamat (String alamat_ksn)
	    {
	        this.alamat_ksn = alamat_ksn;
	    }

	    public String getKsnAlamat()
	    {
	        return alamat_ksn;
	    }
	    
	    
	    
	    public void setKsnHarga (String harga)
	    {
	        this.harga = harga;
	    }

	    public String getKsnHarga()
	    {
	        return harga;
	    }
	    
	    
	    
	    public void setKsnTelpon (String telpon)
	    {
	        this.telpon = telpon;
	    }

	    public String getKsnTelpon()
	    {
	        return telpon;
	    }
}
